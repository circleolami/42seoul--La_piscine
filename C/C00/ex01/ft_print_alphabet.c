#include <unistd.h>

void	ex00_ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char	i;

	i = 'a';
	while (i <= 'z')
	{
		ex00_ft_putchar(i);
		i++;
	}
	return ;
}
